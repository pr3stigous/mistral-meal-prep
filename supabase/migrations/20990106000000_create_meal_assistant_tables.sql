-- Phase 5: Create Storage Bucket and Policies
-- Create a private bucket for recipe images.
INSERT INTO storage.buckets (id, name, public)
VALUES ('recipe_images', 'recipe_images', false)
ON CONFLICT (id) DO NOTHING;

-- Add RLS policies for the 'recipe_images' bucket.
-- Users can view their own images.
CREATE POLICY "Users can view their own recipe images"
ON storage.objects FOR SELECT
TO authenticated
USING (bucket_id = 'recipe_images' AND (storage.foldername(name))[1] = (select auth.uid())::text);

-- Users can upload images into their own folder.
CREATE POLICY "Users can upload to their own recipe folder"
ON storage.objects FOR INSERT
TO authenticated
WITH CHECK (bucket_id = 'recipe_images' AND (storage.foldername(name))[1] = (select auth.uid())::text);

-- Users can update their own images.
CREATE POLICY "Users can update their own recipe images"
ON storage.objects FOR UPDATE
TO authenticated
USING (bucket_id = 'recipe_images' AND (storage.foldername(name))[1] = (select auth.uid())::text);

-- Users can delete their own images.
CREATE POLICY "Users can delete their own recipe images"
ON storage.objects FOR DELETE
TO authenticated
USING (bucket_id = 'recipe_images' AND (storage.foldername(name))[1] = (select auth.uid())::text); 